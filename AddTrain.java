    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String tno= trno.getText();
        String tname= trname.getText();
        String doj= date.getText();
        String src= source.getText();
        String desti= dest.getText();
        String cost= price.getText();
        
        
        
        try {
            pst= con.prepareStatement("insert into traindetails(trno,trname,date,source,dest,price)values(?,?,?,?,?,?)");
            pst.setString(1, tno);
            pst.setString(2, tname);
            pst.setString(3, doj);
            pst.setString(4, src);
            pst.setString(5, desti);
            pst.setString(6, cost);
            int k= pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Record successfully added!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Record not added!");
            }
            
            
            trno.setText("");
            trname.setText("");
            date.setText("");
            source.setText("");
            dest.setText("");
            price.setText("");
            trno.requestFocus();
           
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

