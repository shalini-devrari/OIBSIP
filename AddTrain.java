    public void Train_details(){
        try {
            pst= con.prepareStatement("select * from traindetails");
            ResultSet r = pst.executeQuery();
            
            ResultSetMetaData rsd= r.getMetaData();
            int c;
            c= rsd.getColumnCount();
            DefaultTableModel d= (DefaultTableModel)jTable1.getModel();
            
            d.setRowCount(0);
            while(r.next()){
                Vector v= new Vector();
                for(int i=0; i<=c; i++){
                    v.add(r.getString("trno"));
                    v.add(r.getString("trname"));
                    v.add(r.getString("date"));
                    v.add(r.getString("source"));
                    v.add(r.getString("dest"));
                    v.add(r.getString("price"));
                    
                    d.addRow(v);
                    
                }
            }
            
             
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
    

